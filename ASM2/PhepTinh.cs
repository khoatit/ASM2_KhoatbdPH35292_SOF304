using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ASM2
{
    public class PhepTinh
    {
        public int Tong(int a, int b)
        {
            return a + b;
        }
        public int Hieu(int a, int b)
        {
            return a - b;
        }
        public int Tich(int a, int b)
        {
            return a * b;
        }
    }
}
